public class StarPattern {
    public static void main(String[] args){
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");


        System.out.println("\n");

        //to print *
        //         * *
        System.out.println("*\n* *");
    }
}
